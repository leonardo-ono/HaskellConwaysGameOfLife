section .text

	start:
			cld ; clear direction flag
			
		.main_loop:
			call print_grid
			
			mov si, 3
			call sleep
			
			; exit if keypress
			mov ah, 1
			int 16h
			jnz .exit
			
			call simulate
			jmp .main_loop
		
		.exit:	
			; return to DOS
			mov ax, 4c00h
			int 21h

	; in:
	;	si = number of 55.56 ms to wait
	sleep:
			mov ah, 0
			int 1ah
			mov bx, dx
		.wait:
			int 1ah
			sub dx, bx
			cmp dx, si
			jl .wait
			ret
					
	print_grid:
			push es
			mov ax, 0b800h
			mov es, ax
			mov si, 0
			mov di, 0
		.next_char:
			mov al, [grid + si]
			
			; convert 1 to filled box char
			cmp al, 0
			jz .char_ok
			mov al, 219
		.char_ok:
		
			mov [es:di], al
			add di, 2
			inc si
			cmp si, GRID_COLS * GRID_ROWS
			jb .next_char
			pop es
			ret
	
	simulate:
			mov si, 0
			
		.next_cell:
			mov cl, 0 ; result
		
			; to keep things simple, 
			; let's just ignore the borders
			mov ax, si
			mov dl, GRID_COLS
			div dl ; al=y / ah=x
			cmp al, 0 
			jz .ignore
			cmp al, GRID_ROWS - 1
			jz .ignore
			cmp ah, 0
			jz .ignore
			cmp ah, GRID_COLS - 1
			jz .ignore
			
			mov al, [grid + si] ; current state
			
			; evaluate neighbors
			xor ah, ah
			mov di, 0
		.next_neighbor:
			mov bp, [neighbors_offset + di]
			add ah, [grid + si + bp]
			add di, 2
			cmp di, 16
			jb .next_neighbor
			
			cmp ax, 201h
			jz .alive
			cmp ax, 301h
			jz .alive
			cmp ax, 300h
			jz .alive
			jmp .ignore
			
		.alive:
			mov cl, 1
			
		.ignore:
			mov [grid_tmp + si], cl ; set result
			
			inc si
			cmp si, GRID_COLS * GRID_ROWS
			jbe .next_cell
			
		.update_grid:
			mov si, grid_tmp
			mov di, grid
			mov cx, GRID_COLS * GRID_ROWS
			rep movsb
			
			ret
	
